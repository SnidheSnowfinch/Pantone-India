    <h2>Introduction:</h2>
    <p>Electronic waste, or e-waste, is a term for electronic products that have become unwanted, non-working or
        obsolete, and have essentially reached the end of their useful life.</p>
    <p>As per E-waste Rule 2016, the E-waste is defined as ‘electrical and electronic equipment, whole or in part
        discarded as waste by the consumer or bulk consumer as well as rejects from manufacturing, refurbishment and
        repair processes.</p>
    <p>E-waste contains many valuable, recoverable materials such as aluminum, copper, gold, silver, plastics, and
        ferrous metals. In order to conserve natural resources and the energy needed to produce new electronic equipment
        from virgin resources, electronic equipment can be refurbished, reused, and recycled instead of being
        landfilled.</p>
    <p>E-waste also contains toxic and hazardous materials including mercury, lead, cadmium, beryllium, chromium, and
        chemical flame retardants, which have the potential to leach into our soil and water.</p>
    <h2>Benefit of Recycling of E-waste:</h2>
    <ul>
        <li>E-waste contains many valuable, recoverable materials such as aluminum, copper, gold, silver, plastics, and
            ferrous metals. In order to conserve natural resources and the energy needed to produce new electronic
            equipment from virgin resources, electronic equipment can be refurbished, reused, and recycled instead of
            being landfilled.</li>
        <li>E-waste also contains toxic and hazardous materials including mercury, lead, cadmium, beryllium, chromium,
            and chemical flame retardants.</li>
        <li><strong>Protects your surroundings -</strong> Safe recycling of outdated electronics promotes sound
            management of toxic chemicals such as lead and mercury.</li>
        <li><strong>Conserves natural resources -</strong> Recycling recovers valuable materials from old electronics
            that can be used to make new products. As a result, we save energy, reduce pollution, reduce greenhouse gas
            emissions, and save resources by extracting fewer raw materials from the earth.</li>
        <li><strong>Helps others -</strong> Donating your used electronics benefits your community by passing on
            ready-to-use or refurbished equipment to those who need it.</li>
        <li><strong>Saves landfill space -</strong> E-waste is a growing waste stream. By recycling these items,
            landfill space is conserved.</li>
    </ul>
